
/**
 * A class which indicates whether a method was successful or not.
 * @class success
 * @module AppModule
 * @submodule Domain
 */
export class success {

  /**
   * The message to print to the console.
   * @type {string}
   * @memberof success
   * @property consoleMessage
   */
  consoleMessage : string;

  /**
   * A boolea indicating whether the operation was successful or not.
   * @type {string}
   * @memberof success
   * @property success
   */
  success : boolean;    
}